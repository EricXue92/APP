def _resolve_lang(text: dict[str, str], lang: str) -> str:
    """Pick the requested language, falling back to zh-Hant then en."""
    return text.get(lang) or text.get("zh-Hant") or text.get("en", "")


def get_level_guide(lang: str) -> list[dict]:
    """Resolve trilingual text to given language and return level groups."""
    result = []
    for group in LEVEL_GROUPS:
        resolved_levels = [
            {
                "level": level["level"],
                "description": _resolve_lang(level["description"], lang),
            }
            for level in group["levels"]
        ]
        resolved_skills = [
            {
                "name": _resolve_lang(skill["name"], lang),
                "description": _resolve_lang(skill["description"], lang),
            }
            for skill in group["skills"]
        ]
        result.append(
            {
                "title": _resolve_lang(group["title"], lang),
                "levels": resolved_levels,
                "skills": resolved_skills,
            }
        )
    return result
